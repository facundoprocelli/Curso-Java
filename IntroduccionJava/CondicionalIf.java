public class CondicionalIf {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;

        if (num1 < num2) {
            System.out.println("Numero 2 es mayor al 1");
        }
        else if(num2 < num1)
        {
            System.out.println("Numero 1 es mayor al 2");

        }
        else {
            System.out.println("Los numeros son inguales");
        }

    }

}
