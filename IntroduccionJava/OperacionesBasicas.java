package IntroduccionJava;


public class OperacionesBasicas {

    
	public static void main(String args[]){

        int num1, num2;
        float resultado;

        num1 = 4;
        num2 = 3;

        resultado = num1 + num2;
        System.out.println("El resultado de la suma es de: " + resultado);
        
        resultado = num1 - num2;
        System.out.println("El resutlado de la resta es: "+ resultado );
        
        resultado = num1 * num2;
        System.out.println("El resutlado de la multiplicacion es: "+ resultado );
        
        resultado = num1 / num2;
        System.out.println("El resutlado de la Division es: "+ resultado );

    }
    
}
